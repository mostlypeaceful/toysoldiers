	b32 tUser::fReadProfile( byte* data, u32 size )
	{
		// Note: Called from a background thread
		if( SteamRemoteStorage( )->FileExists( cProfileFileName ) )
		{
			u32 fileSize = SteamRemoteStorage( )->GetFileSize( cProfileFileName );
			sigassert( fileSize <= size && "Profile file size exceeds given buffer" );
			if( fileSize <= size )
			{
				log_line( Log::cFlagNone, "Reading " << cProfileFileName << " from Steam Cloud" );
				SteamRemoteStorage( )->FileRead( cProfileFileName, data, size );
				return true;
			}
		}
		log_line( Log::cFlagNone, "File " << cProfileFileName << " does not exist in Steam Cloud" );
		return false;
	}

	b32 tUser::fWriteToProfile( byte* data, u32 size )
	{
		// Note: Called from a background thread
		log_line( Log::cFlagNone, "Writing " << cProfileFileName << " to Steam Cloud" );
		return SteamRemoteStorage( )->FileWrite( cProfileFileName, data, size );
	}

