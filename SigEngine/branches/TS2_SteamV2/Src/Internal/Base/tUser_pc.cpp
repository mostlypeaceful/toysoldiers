	b32 tUserPic::fSetData( byte* data, u32 width, u32 height, tPixelFormat pixelFormat )
	{
		// create the texture
		const Gfx::tDevicePtr & device = Gfx::tDevice::fGetDefaultDevice( );

		IDirect3DDevice9* d3ddev = device->fGetDevice( );
		IDirect3DTexture9* d3dtex = 0;
		HRESULT hr = d3ddev->CreateTexture( width, height, 1, 0, D3DFMT_A8R8G8B8, D3DPOOL_MANAGED, &d3dtex, 0 );
		sigassert( SUCCEEDED( hr ) && d3dtex );

		// lock the top mip
		D3DLOCKED_RECT rect;
		auto result = d3dtex->LockRect( 0, &rect, 0, 0 );
		if( SUCCEEDED( result) )
		{
			byte* dst = ( byte* )rect.pBits;
			byte* src = data;
			switch( pixelFormat )
			{
			case cPixelFormatRGBA:
				for( u32 y = 0; y < height; ++y )
				{
					// Swap R and B
					for( u32 x = 0; x < width; ++x )
						fSwap( src[ x << 2 ], src[ ( x << 2 ) + 2 ] );
					// Copy to texture
					fMemCpy( dst, src, width * 4 );
					dst += width * 4; //rect.Pitch;  // Pitch was returning zero
					src += width * 4;
				}
				break;

			case cPixelFormatRGB:
				for( u32 y = 0; y < height; ++y )
				{
					// Swap R and B and add A
					for( u32 x = 0; x < width; ++x )
					{
						*dst++ = src[ 2 ];
						*dst++ = src[ 1 ];
						*dst++ = src[ 0 ];
						*dst++ = 255;
						src += 3;
					}
				}
				break;
			}

			Gfx::fReleaseComPtr((IDirect3DTexture9*&)mTexture);

			d3dtex->UnlockRect( 0 );

			mTexture = d3dtex;
			mTextureDims.x = ( f32 )width;
			mTextureDims.y = ( f32 )height;
			mState = cStateDone;
			return true;
		}
		return false;
	}

