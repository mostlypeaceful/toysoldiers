	void tUserPic::fStartReadPicture( )
	{
		sigassert( mState == cStateReadingKey );

		// Read the picture data
		int handle = 0;
		if( mRequest.mSmall )
			handle = SteamFriends( )->GetSmallFriendAvatar( mRequest.mGamer );
		else
			handle = SteamFriends( )->GetMediumFriendAvatar( mRequest.mGamer );

		if( handle > 0 )
		{
			u32 width, height;
			if( SteamUtils( )->GetImageSize( handle, &width, &height ) )
			{
				tDynamicBuffer data( width * height * 4 );
				if ( SteamUtils( )->GetImageRGBA( handle, data.fBegin( ), data.fTotalSizeOf( ) ) )
				{
					if( fSetData( data.fBegin( ), width, height, cPixelFormatRGBA ) )
						return;
				}
			}
		}

		// Use the default gamerpic
		if( fSetData( sDefaultGamerPicMedium, 64, 64, cPixelFormatRGB ) )
			return;

		// All failed, so release them and give up
		fReleaseTextures( );
		mState = cStateNull;
	}

